"""
bezier.py
---------
حساب منحنيات Bezier التكعيبية (Cubic Bezier) المستخدمة في رسم خطوط
الباترون المنحنية (مثل خط الجانب وخط الداخلية وخط القُب).

هذا الكلاس نسخة مباشرة من منطقك الأصلي في matplotlib، بدون أي تعديل
في المعادلات الرياضية، لضمان تطابق النتائج 100%.
"""

import numpy as np


class BezierCurve:
    @staticmethod
    def compute_cubic_bezier(p0, p1, p2, p3, num_points: int = 100) -> np.ndarray:
        """
        يحسب نقاط منحنى Bezier تكعيبي بين 4 نقاط تحكم:
        p0: نقطة البداية
        p1, p2: نقطتا التحكم (تحددان انحناء الخط)
        p3: نقطة النهاية

        المعادلة الكلاسيكية:
        B(t) = (1-t)^3 * p0 + 3(1-t)^2 t * p1 + 3(1-t) t^2 * p2 + t^3 * p3
        """
        t = np.linspace(0, 1, num_points)
        x = (
            (1 - t) ** 3 * p0[0]
            + 3 * (1 - t) ** 2 * t * p1[0]
            + 3 * (1 - t) * t ** 2 * p2[0]
            + t ** 3 * p3[0]
        )
        y = (
            (1 - t) ** 3 * p0[1]
            + 3 * (1 - t) ** 2 * t * p1[1]
            + 3 * (1 - t) * t ** 2 * p2[1]
            + t ** 3 * p3[1]
        )
        return np.column_stack((x, y))
